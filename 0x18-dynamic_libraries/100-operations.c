#include <stdio.h>

/**
 * add - addition
 * @a: num 1
 * @b: nul 2
 * Return: operation
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * sub - subtraction
 * @a: num 1
 * @b: nul 2
 * Return: operation
 */

int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - multiplication
 * @a: num 1
 * @b: nul 2
 * Return: operation
 */

int mul(int a, int b)
{
return (a * b);
}

/**
 * div - division
 * @a: num 1
 * @b: nul 2
 * Return: operation
 */

int div(int a, int b)
{
return (a / b);
}

/**
 * mod - modulo
 * @a: num 1
 * @b: nul 2
 * Return: operation
 */

int mod(int a, int b)
{
return (a % b);
}
